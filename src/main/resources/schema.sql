CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL,
  CONSTRAINT pk_user PRIMARY KEY (id),
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);
CREATE TABLE IF NOT EXISTS requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  description VARCHAR(512) NOT NULL,
  requestor BIGINT REFERENCES users(id) ON DELETE CASCADE,
  created TIMESTAMP WITHOUT TIME ZONE,
  CONSTRAINT pk_requests PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  description VARCHAR(512) NOT NULL,
  available BOOLEAN NOT NULL,
  user_id BIGINT REFERENCES users(id) ON DELETE CASCADE,
  request_id BIGINT REFERENCES requests(id) ON DELETE SET NULL,
  CONSTRAINT pk_item PRIMARY KEY (id)
);
CREATE TABLE IF NOT EXISTS bookings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  started TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  ended TIMESTAMP WITHOUT TIME ZONE NOT NULL CHECK (started<ended),
  status VARCHAR(8) NOT NULL,
  item_id BIGINT REFERENCES items(id) ON DELETE CASCADE,
  booker BIGINT REFERENCES users(id) ON DELETE CASCADE,
  CONSTRAINT pk_book PRIMARY KEY (id)
);
CREATE TABLE IF NOT EXISTS comments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  text VARCHAR(512) NOT NULL,
  item_id BIGINT REFERENCES items(id) ON DELETE CASCADE,
  author BIGINT REFERENCES users(id) ON DELETE CASCADE,
  CONSTRAINT pk_comments PRIMARY KEY (id)
);